    public void mostrarDatos(){
    conexion conec = new conexion();
        while(modelo.getRowCount()>0){
        modelo.removeRow(0);
        }
            try {
                String sentencia = String.format("SELECT * FROM deterministicos WHERE idAceptador == '%s'",idAc);
                  ResultSet resultado = conec.consultar(sentencia);
                  while(resultado.next()){
                  Object[] objAceptador = {resultado.getString("idDeter"), resultado.getString("estadoAc"), resultado.getString("estado0"),resultado.getString("estado1"),resultado.getString("esInicial"),resultado.getString("esAceptador"),resultado.getString("idAceptador")};
                  modelo.addRow(objAceptador);
                  }
        } catch (Exception e) {
            System.out.println("Error :"+ e);
        }
    }
