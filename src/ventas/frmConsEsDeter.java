        String aux1 = txt1.getText();
        String auxInicial = txtInicial.getText();
        String auxAcept = txtAcep.getText();
        String auxAc = txtEsAc.getText();
        conexion conec = new conexion();
        String sentencia = String.format("UPDATE aceptadores SET estadoAc = '%s', estado0 = '%s', estado1 = '%s', esInicial = '%s', esAceptador = '%s' WHERE idEstado == '%s'", auxAc, aux0, aux1, auxInicial, auxAcept, auxId);
